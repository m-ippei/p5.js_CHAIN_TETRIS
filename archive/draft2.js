function checkField(){
	for(var k=0;k<4;k++){
		for(var l=0;l<4;l++){
			if(fallBlock[l][k]&&field[l+y][k+x]){
				return false
			}
		}
	}
	updateField()
}

function updateField(){
	field = empty_field
	for(var i=0;i<4;i++){
		for(var j=0;j<4;j++){
			if(field[j+y][i+x]===0){
				field[j+y][i+x] = fallBlock[j][i]
			}
		}
	}
}

function drawField(){
	for(var i=0;i<12;i++){
		for(var j=0;j<22;j++){
			if(field[j][i] === 1){
				fill(0,200,200)
			}else if(field[j][i] === 2){
				fill(30,150,200)
			}else if(field[j][i] === 3){
				fill(30,200,100)
			}else{
				fill(100,100,0)
			}
			rect(i*10,j*10,10,10)
		}
	}
}



